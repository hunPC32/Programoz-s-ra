szam1 = int(input("Szám1: "))
muvelet = input("Művelet: ")
szam2 = int(input("Szám2: "))

if muvelet == "+":
    print(szam1+szam2)
if muvelet == "-":
    print(szam1-szam2)
if muvelet == "*":
    print(szam1*szam2)
if muvelet == "/":
    print(szam1/szam2)
if muvelet == "//":
    print(szam1//szam2)
if muvelet == "%":
    print(szam1%szam2)